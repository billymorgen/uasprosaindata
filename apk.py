import streamlit as st
def main():
    st.title("Aplikasi Web Streamlit")
    st.header("Selamat datang!")
    st.write("Ini adalah contoh aplikasi web menggunakan Streamlit.")
    st.image("gambar.png", caption="Gambar contoh")
    st.write("Terima kasih telah menggunakan aplikasi ini!")

if __name__ == '__main__':
    main()
